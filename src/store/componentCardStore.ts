/**
 * Component Card Store — src/store/componentCardStore.ts
 *
 * Phase CV2.3: Component Cards on Canvas
 *
 * Owns the state for all component cards displayed on the infinite canvas in
 * 'build' and 'govern' canvas view modes. Deliberately kept separate from
 * canvasStore, which owns single-file editing lifecycle state (activeFilePath,
 * saveState, mithrilViolations). The card system is orthogonal:
 *
 *   - cards[]           — All indexed components in the active project.
 *   - selectedCardId    — ID of the currently selected card (independent of
 *                         canvasStore.activeSelection, which is a data-flint-id
 *                         within a single file).
 *   - cardPositions     — Persisted spatial positions keyed by card ID.
 *   - isLoaded / isLoading — Fetch lifecycle flags.
 *
 * Cross-store coordination rule (Commandment anti-pattern):
 *   DO NOT import canvasStore here. When selectedCardId changes, a React effect
 *   in App.tsx reads the new ID, finds the card, and calls:
 *     canvasStore.setActiveFile(card.filePath)
 *     canvasStore.setRightTab('properties')
 *
 * Position persistence:
 *   updatePosition()  — Updates in-memory map immediately.
 *   savePositions()   — Debounced (500 ms). Calls window.flintAPI.components.savePositions().
 *   loadCards()       — Calls window.flintAPI.components.loadPositions() and merges
 *                       saved positions. Cards without saved positions get auto-layout
 *                       positions computed from the grid algorithm below.
 *
 * Auto-layout algorithm (first open, or no .flint/card-positions.json):
 *   1. Group by category: primitive → molecule → organism → page → layout → uncategorized.
 *   2. Within each group, sort alphabetically by name.
 *   3. Grid: 4 columns, 240px column width, 280px row height, 40px gap.
 *   4. Each category group starts on a new row with 60px vertical gap between groups.
 *
 * React Flow selectors:
 *   toFlowNodes() — Maps cards + positions to Node<ComponentCardNodeData>[].
 *   toFlowEdges() — Maps card.dependencies to Edge<DependencyEdgeData>[] for Govern mode.
 *
 * Commandment compliance:
 *   C3  — No Array.map in this store; IDs are stable hashes from the IPC layer.
 *   C12 — All mutations use set() with spread — never mutate objects in place.
 *   Process boundary — No fs/node: imports. All IPC through window.flintAPI.
 */

