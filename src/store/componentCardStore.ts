/**
 * The five predefined governance sticker types.
 * Placed on component cards in Govern mode to communicate workflow status
 * at a glance without opening the PropertiesPanel.
 */
export type GovernanceStickerType = 'needs-review' | 'approved' | 'deprecated' | 'wip' | 'blocked'

/**
 * A single governance sticker attached to a component card.
 * V1: in-memory only. Persistence via the annotation engine is a future phase.
 */
export interface GovernanceSticker {
    /** UUID — generated by addSticker. */
    id: string
    /** One of the five predefined sticker types. */
    type: GovernanceStickerType
    /** The component card this sticker is pinned to. */
    componentId: string
    /** Optional free-text note from the person who placed the sticker. */
    note?: string
    /** ISO 8601 timestamp of when the sticker was placed. */
    createdAt: string
}

